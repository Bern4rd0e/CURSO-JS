function updateDateIf3PM() {
    var agora = new Date();
    var day = agora.getDate();
    var month = agora.getMonth() + 1; // getMonth() returns 0-11, so we add 1
    var year = agora.getFullYear();

    if (agora.getHours() >= 15) {
        day += 1;

        // Ajuste do dia e mês automaticamente
        var newDate = new Date(year, month - 1, day); // month - 1 because Date months are 0-11

        day = newDate.getDate();
        month = newDate.getMonth() + 1; // Ajusta novamente, pois newDate ajusta o mês corretamente
        year = newDate.getFullYear();
    }

    document.getElementById("current_date").innerHTML = day + "/" + month + "/" + year;
}

